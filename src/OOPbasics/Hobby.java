package OOPbasics;


class Hobby {
    String name;
    String hobbies;
    
    Hobby(String name, String hobbies) {
        this.name = name;
        this.hobbies = hobbies;
  
    }
    String getName() {
        return name;
    }
    String getHobbies() {
        return hobbies;
    }
 
}
    

